#Example for variable assignment and printing
name = "Alice"
age = 30
print(name)
print(age)

#Example of numbers and basic arithmetic
x = 10
y = 5
sum = x + y
difference = x - y
product = x * y
quotient = x / y
print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
#Round function example
pi = 3.14159
rounded_pi = round(pi, 2)
print("Rounded pi:", rounded_pi)

#Example of string
greeting = "Hello, World!"
print(greeting)
#String slicing
first_five = greeting[:5]
print("First five characters:", first_five)
#String indexing
first_character = greeting[0]
print("First character:", first_character)
#String concatenation
name = "Alice"
welcome_message = "Welcome, " + name + "!"
print(welcome_message)
#Typecasting
age = 30
age_str = str(age)
print("Age as a string:", age_str)      

# Example of List
my_list = [1, 2, 3]
my_list.append(4)
print("List after append:", my_list)
another_list = [5, 6]
combined = my_list + another_list
print("Combined list:", combined)
print("Length of combined list:", len(combined))
print("Is 7 in combined list?", 7 in combined)